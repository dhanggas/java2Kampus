    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            int gaji ,tunjangan, overtime, gajiBersih;
            gaji=Integer.parseInt(txtGpokok.getText());
            tunjangan=Integer.parseInt(txtTunjangan.getText());
            overtime=Integer.parseInt(txtOver.getText());
            gajiBersih=gaji+tunjangan+overtime;
            
            kon=Koneksi.getConnection();
            sql = "insert into tbtransaksi values "
                + "('" + txtNip.getText() + "',"                
                + "'" + txtKdjab.getText() + "',"
                + "'" + txtOver.getText() + "',"
                + "'" + gajiBersih + "')";
            stat=kon.createStatement();
            stat.execute(sql);
            hapusField();
            JOptionPane.showMessageDialog(null, "Data tersimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

