    private void btnCari1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCari1ActionPerformed
        // TODO add your handling code here:
        try {
        kon = Koneksi.getConnection();
        sql = "select * from tbpegawai where nip='" + txtNip.getText() + "'";
        stat = (Statement) kon.createStatement();
        rs = stat.executeQuery(sql);
        while (rs.next()) {
            txtNama.setText(rs.getString("nama"));
            txtGpokok.setText(rs.getString("gpokok"));
            JOptionPane.showMessageDialog(null, "Data di temukan");
        }
    } catch (SQLException ex) {
        Logger.getLogger(FrmTransaksi.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnCari1ActionPerformed

